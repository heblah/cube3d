#include <t_cube3d.h>
#include <ft_cube3d.h>

void getcolumns(t_data *data)
{
	data->lineheight = W_HEIGHT / data->walldist;
	data->drawstart = - data->lineheight / 2 + W_HEIGHT / 2;
	if (data->drawstart < 0)
		data->drawstart = 0;
	data->drawend = data->lineheight / 2 + W_HEIGHT / 2;
	if (data->drawend >= W_HEIGHT)
		data->drawend = W_HEIGHT - 1;
}
